</p> मला, (रुग्णाचे नाव --------------------------) समजेल अशा भाषेत मला समजावून सांगण्यात आले आहे की माझ्या उजव्या डाव्या /दोन्ही डोळ्याच्या अंतर्भागात रक्तस्त्राव ( vitreous haemorrhage ) झाला आहे आणि त्याकरिता पार्स प्लाना व्हिट्रेक्टोमी या शस्त्रक्रियेची गरज आहे. <p>

</p> माझ्या दृष्टीपटलाची (रेटीनाची तपासणी करण्यासाठी हे रक्त काढणे जरुरीचे आहे. या शस्त्रक्रियेचे काही धोके अथवा गुंतागुंती (कॉम्प्लिकेशन्स) असू शकतात. डोळ्यामध्ये परत रक्तस्त्राव होणे (शस्त्रक्रियेनंतर लगेच किंवा काही कालावधीनंतर), दृष्टीपटल सरकणे (retinal detachment), डोळ्याच्या अंतर्भागात जंतुसंसर्ग होणे (endophthalmitis), सिलीकॉन ऑईल इंजेक्शन देण्याची गरज भासणे, काचबिंदू, मोतीबिंदू, बुबुळाची झीज, बुबुळावर पांढरा पडदा येणे, नजरेत सुधारणा न होणे किंवा ती आणखी कमी होणे. दुसऱ्या डोळ्याच्या अंतर्भागात तीव्र सूज (सिम्पॅथिटीक ऑफ्थॅल्मायटीस) झाल्यास त्यावर शस्त्रक्रिया किंवा उपचार करण्याची गरज भासू शकणे, फेरशस्त्रक्रियेची आणि दुसऱ्या डोळ्याच्या शस्त्रक्रियेची गरज भासू शकणे इत्यादी धोके होऊ शकतात.<p>

</p>ही शस्त्रक्रिया डोळ्यापुरती भूल देऊन किंवा गरज भासल्यास संपूर्ण भूल देऊन (local anaesthesia or general anaesthesia ) केली जाते. डोळ्याभोवती देण्यात येणाऱ्या स्थानिक भूलीचे दुष्परिणाम पुढे नमूद केल्याप्रमाणे असू शकतात. डोळ्याला किरकोळ जखमा होणे, डोळे लाल होणे, डोळ्यावर सूज येणे, डोळ्यामागे रक्तस्त्राव होणे, पडद्यावर रक्तस्त्राव होणे, डोळ्याला छिद्र पडणे, स्नायूतील विषबाधा (myotoxicity). भूलीच्या गुंतागुंती (कॉम्प्लिकेशन्स) म्हणून हृदयाच्या कार्यात बिघाड (oculocardiac reflex) होऊ शकतो. <p>

