</p> या शस्त्रक्रियेमुळे दुसऱ्या डोळ्यात रक्तस्त्राव होण्याचे प्रमाण कमी होत नाही अथवा टळू शकत नाही. दुसऱ्या डोळ्यात रक्तस्त्राव झाल्यास ही शस्त्रक्रिया आणि योग्य उपचार दुसऱ्या डोळ्यातही करावे लागू शकतात.<p>

</p> माझ्या डॉक्टरांनी मला माझ्या डोळ्याची परिस्थिती, शस्त्रक्रियेचे फायदे आणि धोके, पर्यायी उपचार याची माहिती दिली आहे. या शस्त्रक्रियेबद्दल प्रश्न विचारण्याची संधी मला मिळाली आहे. या शस्त्रक्रियेच्या पर्यायी उपचाराबद्दल माझे समाधान होईल अशी माहिती डॉक्टरांनी मला दिली आहे. मी हे समजू शकतो/शकते की या शस्त्रक्रियेमुळे अंधत्व टाळण्याची खात्री देता येत नाही. ही शस्त्रक्रिया परत करण्याची गरज भासू शकते.<p>

